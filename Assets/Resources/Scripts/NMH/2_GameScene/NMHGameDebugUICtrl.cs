using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;

public class NMHGameDebugUICtrl : MonoBehaviour
{
    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    //아래는 public 변수

    public Text txtCurWaveState;
    public Text txtCurWave;

    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    //아래는 Unity 함수

    void Start ()
    {
		
	}
	
	void Update ()
    {
        SetDebugText();
    }

    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////
    //아래는 일반 public 함수

    public void SetDebugText()
    {
        txtCurWaveState.text = "Current Wave State : " + NMHWaveMng.instance.GetWaveState().ToString();
        txtCurWave.text = "Current Wave : " + NMHWaveMng.instance.GetCurWave().ToString();
    }
}
